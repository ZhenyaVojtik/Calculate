public class Action {

    public static double summ(double a, double b){
        return a+b;
    }

    public static double minus(double a, double b)
    {
        return a - b;
    }

    public static double mult(double a, double b)
    {
        return a*b;
    }

    public static double devide(double a, double b)
    {
        return a/b;
    }


}
